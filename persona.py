class Persona:
    def __init__(self, nombre, edad, altura, sexo):
        self.nombre = nombre
        self.edad = edad
        self.altura = altura
        self.sexo = sexo

    def getNombre (self):
        return self.nombre
    
    def setNombre (self, p):
        self.nombre = p

    def getEdad (self):
        return self.edad
    
    def setEdad (self, p):
        self.edad = p

    def getAltura (self):
        return self.altura
    
    def setAltura (self, p):
        self.altura = p

    def getSexo (self):
        return self.sexo
    
    def setSexo (self, p):
        self.sexo = p




